#include <stdlib.h>
#include <stdio.h>
#include <string.h>

int main()
{
    system("clear");
    int valueOne = 23;
    int valueTwo = 10;
    printf("Soma: %i\n", valueOne + valueTwo);          // Soma
    printf("Subtração: %i\n", valueOne - valueTwo);     // Subtração
    printf("Multiplicação: %i\n", valueOne * valueTwo); // Multiplicação
    printf("Divisão: %i\n", valueOne / valueTwo);       // Divisão

    printf("\n\n");
    return;
    system("pause");
}